package com.ddt.cinefsoft.model;

public class Auditorium {

	private int id;
	private String name;

	public Auditorium() {
		super();
	}

	public Auditorium(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

}
